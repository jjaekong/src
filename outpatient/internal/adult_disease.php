                    많은 환자들이 식사요법을 실천하기 어려운 것으로 생각하고 있으며,  더러는 특정한 음식을 금하는 것, 혹은 보리밥, 잡곡밥만을 먹고 고기는 절대로 먹지 않는 것이 식사요법이라고 잘 못 알고 있기도 합니다. 당뇨병 환자의 식사요법 원칙은 영양소를 골고루 배분해 적절한 열량을 섭취하는 것이며, 지나치게 열량이 높은 음식은 피하는 것이 좋습니다. 
                </p>
                <h3>운동관리</h3>
                <ul class="arrow-list">
                    <li>당뇨병 환자에서 운동요법이란 식사요법, 약물요법과 더불어 당뇨병 치료에 중요한 한 부분입니다.</li>
                    <li>운동 요법은 식사 후 30분-1시간 사이에 1회에 30-45분 정도 일주일에 4-5일 시행하는 것을 권장합니다.</li>
                </ul>
