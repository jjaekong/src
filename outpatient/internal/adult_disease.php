                    <h2>당뇨병의 급성 합병증은?</h2>
                    <p>
                        당뇨병의 치료 과정중 가장 흔히 나타나는 급성합병증으로 저혈당증을 들 수 있습니다. 사람에 따라서 저혈당 증상이 나타나는 혈당은 일정치 않으나 제대로 혈당이 50mg/dl 이하로 떨어졌을 때 저혈당 증상이 나타납니다. 정상적인 경우 며칠 동안 단식을 하더라도 혈당이 50mg/dl 이하로는 내려가지 않습니다. 그러나 약물요법을 하는 당뇨병 환자의 경우, 평소보다 식사를 적게 하거나, 식사를 거르는 경우, 평소보다 운동량이 너무 많은 경우, 실수로 인슐린 주사를 많이 주사한 경우, 경구용 혈당 강하제를 잊고 또다시 복용한 경우에 저혈당증이 나타납니다. 그 외 식사시간의 지연, 음주 등의 원인에 의해서도 저혈당증이 나타납니다.
                    </p>
