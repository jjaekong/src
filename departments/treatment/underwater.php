                <div class="section-content">
                    <p>
                        물의 물리적 특성을 주로 이용하여 환자의 통증과 장애를 치료하고 예방하는데 이용되어지고 있습니다.  수중에서 운동치료 효과는 등통과 근경축의 완화, 관절운동범위의 유지 또는 증가,  마비된 근육의 재교육, 순환의 개선, 기능적 활동의 촉진, 균형 및 협응성과 자세의 유지 및 개선등이 있으며, 또한 수중에서는 물밖에서 성취할 수 있는 운동의 기능성을 제공할 분만 아니라 상당한 수준으로 시각과 청각, 피부, 고유수용기 및 열에 의한 자극으로 지각을 촉진 할 수 있습니다. 
                    </p>
                    <p>
                        수중에서 누구의 도움 없이 독립적일 수 있다는 것과 물 밖에서는 하기 어렵거나 불가능한 동작들을 가능하게 할 수 있는 능력은 사화생활에서 사람들을 우호적으로 만들고 지속적인 심리적 만족감을 주며, 자신감을 북돋아줌으로써, 사회생활에 도움이 되는 유익한 결과를 가져옵니다.
                    </p>
