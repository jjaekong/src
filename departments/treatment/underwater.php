                            <dl class="inline-dl">
                                <dt>와츠(WATSU)</dt>
                                <dd>
                                    와츠 (WATSU, Water Shiatsu) 는 Harold Dull 에 의해서 캘리포니아 하빈 온천 (Harbin Hot Springs) 에서 개발되었습니다. 따뜻한 물에서 기울기의 중심섬 (Merdian)과 에너지점 (Charkra)을 자극하는 스트레칭과 지압을 통해 심신의 해방감, 최고로 편해진 감각, 이런 심신의 에너지 흐름을 느끼고, 그 및 신경, 정신 등의 완벽한 이완을 통하여 신체적인 편안함과 정신적인 안정감을 유도해내는 방법입니다. 
                                </dd>
                            </dl>
                        </li>
                        <li>
                            <dl class="inline-dl">
                                <dt>바드라가즈 링(The Bad Ragaz Ring Method)</dt>
                                <dd>
                                    BPPM은 1930년 바드라가즈에서 개발되었습니다. 바드라가즈링은 움직임의 범위, 긴장완화, 늘임, 지구력, 특별한 저항 패턴들을 이용하여 근육을 재교육하는 방법으로 등척성, 등장성, 등속성 운동과 수동적 활동이 포함되어 정상적인 운동패턴의 형식과 신경근을 촉진하기 위한 기초적인 감각 수용기의 혼합으로 이루어져 있습니다.
                                </dd>
                            </dl>
                        </li>
                        <li>
                            <dl class="inline-dl">
                                <dt>할리윅(The Halliwick)</dt>
                                <dd>
                                    할리윅 방법은 1930년대 여성들을 위한 학교에서 James Mcmillian에 의해서 개발되었으며, 이 할리윅 기법은 유체역학의 원리와 인체발달에 기초를 두고 있습니다. 물의 부력 원리와 인체의 회원원리를 기초로 치료목적으로 이용되고 있는데 이는 정신지체 장애인의 수영교육을 위해 만들어졌으며, 이후 소아과, 신경학, 류마티스, 정형외과 분야에서 약간 근육분, 관절가동범위(ROM)의 증가, 자세교정, 균형반응촉진, 통증 및 경직의 감소를 위해 사용하는 수중요법입니다. 
                                </dd>
                            </dl>
                        </li>
