                    <p>
                        회복기의 중추신경계 손상환자에게 적용하는 수중재활 프로그램 수중운동치료의 방법에 있어서 대상자의 몸 상태와 향상되는 단계에 따라 대표적인 3가지 치료프로그램을 각기 다르게 적용하고 있습니다.
                    </p>
                    <ul class="arrow-list">
